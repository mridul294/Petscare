    private void signupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupButtonActionPerformed
        
        if(firstName.getText().isEmpty() || ComboBoxForGender.getSelectedIndex() == -1 || lastName.getText().isEmpty() || userName.getText().isEmpty() || Password.getText().isEmpty() || Phone.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Missing Information", "Error", JOptionPane.WARNING_MESSAGE);
        
        else if (!Phone.getText().matches("(05)([0-9]{8})"))
                JOptionPane.showMessageDialog(this, "Invalid Phone Number: Please Enter 10 digit Start with 05 only", "Invalid Phone Number", JOptionPane.ERROR_MESSAGE);
        else if (!Email.getText().matches("^(.+)@(\\S+)$"))
            JOptionPane.showMessageDialog(this, "Invalid Email: Please Check your Entery", "Invalid Invalid Email", JOptionPane.ERROR_MESSAGE);
        else
        {
            try
            {
                if(!(userName.getText().isEmpty()))
                {
                    con =DriverManager.getConnection("jdbc:derby://localhost:1527/petsCare","root","root" );
                    pst = con.prepareStatement("SELECT * FROM Customer where Username = ? ");
                    pst.setString(1,userName.getText());
                    ResultSet rs = pst.executeQuery();

                    if(rs.next())
                        JOptionPane.showMessageDialog(this, "Username Already Exsit !!", "Username Error", JOptionPane.WARNING_MESSAGE);
                    else
                    {
                        String query= "INSERT INTO Customer (FirstName , LastName, Gender, UserName, Password, Phone, Email) VALUES(?,?,?,?,?,?,?)";
                        con =DriverManager.getConnection("jdbc:derby://localhost:1527/petsCare","root","root" );
                        pst =con.prepareStatement(query);
                        pst.setString(1, firstName.getText());
                        pst.setString(2, lastName.getText());
                        pst.setString(3, (ComboBoxForGender.getSelectedIndex() == 0) ? "M" : "F");
                        pst.setString(4, userName.getText());
                        

                        String pwd = "";
                        for(char x : Password.getPassword())
                        pwd += x;
                        pst.setString(5, pwd);

                        pst.setString(6, Phone.getText());
                        pst.setString(7, Email.getText());
                        pst.executeUpdate();
                        JOptionPane.showMessageDialog(null, "SUCCESSFULLY ADDED !");
                    }
                }
            }

            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);
            }

            catch (NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(this, "You Enter Wrong Entery Type", "Error", JOptionPane.WARNING_MESSAGE);
            }

            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_signupButtonActionPerformed

