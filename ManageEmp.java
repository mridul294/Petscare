    public void fillTable()
    {
        try
        {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/PetsCare","root","root");
            st=con.createStatement();
            rs=st.executeQuery("SELECT * FROM Employee");
            
            Emp_Table.setModel(DbUtils.resultSetToTableModel(rs));
            admIDTxt.setText(String.valueOf(currentAdminID));
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(ManageEmp.this, ex);
        }
    }
    
    public void closeDB()
    {
        try
        {
            rs.close();
            st.close();
            con.close();
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(ManageEmp.this, ex);
        }
    }

