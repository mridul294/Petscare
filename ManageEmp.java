    private void Emp_TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Emp_TableMouseClicked
        // TODO add your handling code here:

        try
        {
            int selectRow = Emp_Table.getSelectedRow();
            int SelectID = Integer.parseInt(Emp_Table.getModel().getValueAt(selectRow,0).toString());

            String query = "SELECT * FROM Employee WHERE EmployeeID = " + SelectID;
            rs = st.executeQuery(query);

            rs.next();
            EmpIDtxt.setText(String.format("%d", rs.getInt("EmployeeID")));
            fNameTxt.setText(rs.getString("FirstName"));
            lNameTxt.setText(rs.getString("LastName"));
            ageTxt.setText(String.format("%d", rs.getInt("Age")));
            String s = rs.getString("Gender");
            char gender = s.charAt(0);
            if(gender == 'M')
                genderBox.setSelectedIndex(0);
            else
                genderBox.setSelectedIndex(1);
            userNameTxt.setText(rs.getString("UserName"));
            passTxt.setText(rs.getString("Password"));
            phoneTxt.setText(String.format("%d", rs.getInt("Phone")));
            hourTxt.setText(String.format("%d", rs.getInt("WorkingHours")));
            salaryTxt.setText(String.format("%d", rs.getInt("Salary")));
            admIDTxt.setText(String.format("%d", currentAdminID));
        }

        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(ManageEmp.this, ex);
        }

    }//GEN-LAST:event_Emp_TableMouseClicked

