    private void petTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_petTableMouseClicked
        // TODO add your handling code here:

        try
        {
            int selectRow = petTable.getSelectedRow();
            int SelectID = Integer.parseInt(petTable.getModel().getValueAt(selectRow,0).toString());

            String query = "SELECT * FROM Pet WHERE PetID = " + SelectID;
            rs = st.executeQuery(query);

            rs.next();
            petIDTxt.setText(String.format("%d", rs.getInt("PetID")));
            nameTxt.setText(rs.getString("Pet_Name"));
            typeTxt.setText(rs.getString("Pet_Type"));
            speciesTxt.setText(rs.getString("Species"));
            priceTxt.setText(String.format("%.2f", rs.getDouble("Price")));
            ageTxt.setText(rs.getString("Age"));
            admIDTxt.setText(String.format("%d", currentAdminID));
            
            String path = rs.getString("ImagePath");
            ImageIcon image = new ImageIcon(getClass().getResource("Icon/" + path));
            Image img = image.getImage();
            Image imgImage = img.getScaledInstance(imageLable2.getWidth(),imageLable2.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon scaledIcon = new ImageIcon(imgImage);
            imageLable2.setIcon(scaledIcon);
            imageLable2.setText(rs.getString("ImagePath"));
        }

        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(ManagePet.this, ex);
        }
    }//GEN-LAST:event_petTableMouseClicked

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        if(ageTxt.getText().isEmpty() || speciesTxt.getText().isEmpty() || nameTxt.getText().isEmpty() || priceTxt.getText().isEmpty() || typeTxt.getText().isEmpty() || imageLable2.getIcon() == null)
            JOptionPane.showMessageDialog(this, "Missing Information", "Error", JOptionPane.WARNING_MESSAGE);
        else
        {
            try
            {
                if(Integer.parseInt(priceTxt.getText()) <= 0)
                {
                    JOptionPane.showMessageDialog(this, "Enter Positive Value", "Price Error", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    PreparedStatement insertQuery = con.prepareStatement("INSERT INTO Pet (Age, Species, Pet_Name, Price, AdminID, Pet_Type, ImagePath)\n" +
                        "VALUES (?, ?, ?, ?, ?, ?, ?)");

                    insertQuery.setString(1, ageTxt.getText());
                    insertQuery.setString(2, speciesTxt.getText());
                    insertQuery.setString(3, nameTxt.getText());
                    insertQuery.setDouble(4, Double.valueOf(priceTxt.getText()));
                    insertQuery.setInt(5, Integer.valueOf(admIDTxt.getText()));
                    insertQuery.setString(6, typeTxt.getText());
                    insertQuery.setString(7, imageLable2.getText());

                    int result = insertQuery.executeUpdate();
                    if(result == 1)
                    JOptionPane.showMessageDialog(ManagePet.this, "Pet: " + nameTxt.getText()  + " is Addedd");

                    closeDB();
                    fillTable();
                }
            }

            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.WARNING_MESSAGE);
            }
            
            catch (NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(this, "You Enter Wrong Entery Type", "Error", JOptionPane.WARNING_MESSAGE);
            }

            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(ManagePet.this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

