    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if(ageTxt.getText().isEmpty() || speciesTxt.getText().isEmpty() || nameTxt.getText().isEmpty() || priceTxt.getText().isEmpty() || typeTxt.getText().isEmpty() || imageLable2.getIcon() == null)
            JOptionPane.showMessageDialog(this, "Missing Information", "Error", JOptionPane.WARNING_MESSAGE);
        else
        {
            try
            {
                if(Integer.parseInt(priceTxt.getText()) <= 0)
                {
                    JOptionPane.showMessageDialog(this, "Enter Positive Value", "Price Error", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    PreparedStatement updateQuery = con.prepareStatement("UPDATE Pet SET Pet_Name = ?, Age = ?, Species = ?, Price = ?, Pet_Type = ?, ImagePath = ? ,AdminID = ? WHERE PetID = ?");

                    updateQuery.setString(1, nameTxt.getText());
                    updateQuery.setString(2, ageTxt.getText());
                    updateQuery.setString(3, speciesTxt.getText());
                    updateQuery.setDouble(4, Double.valueOf(priceTxt.getText()));
                    updateQuery.setString(5, typeTxt.getText());
                    updateQuery.setString(6, imageLable2.getText());
                    updateQuery.setInt(7, Integer.valueOf(admIDTxt.getText()));
                    updateQuery.setInt(8, Integer.valueOf(petIDTxt.getText()));

                    int result = updateQuery.executeUpdate();
                    if(result == 1)
                    JOptionPane.showMessageDialog(ManagePet.this, "Selected Recored is Updated");
                    else
                    JOptionPane.showMessageDialog(ManagePet.this, "Failed: Selected Recored is not Updated");

                    closeDB();
                    fillTable();
                }
            }

            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(ManagePet.this, ex.getMessage());
            }
            
            catch (NumberFormatException ex)
            {
                JOptionPane.showMessageDialog(this, "You Enter Wrong Entery Type", "Error", JOptionPane.WARNING_MESSAGE);
            }

            catch(Exception ex)
            {
                JOptionPane.showMessageDialog(ManagePet.this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_editButtonActionPerformed

